function pickUpKanji($helpUTF8){
	// Script to pickup kanji words in help.txt
	preg_match_all('/[\x{4E00}-\x{9FFF}]+/u', $helpUTF8, $m);
	$words=array();
	for($i=0;$i<count($m[0]);$i++) $words[$m[0][$i]]=strlen($m[0][$i]);
	arsort($words);

	ob_start();
	print_r($words);
	$text=ob_get_contents();
	ob_end_clean();
	file_put_contents('./kanjiList.txt',$text);
}
